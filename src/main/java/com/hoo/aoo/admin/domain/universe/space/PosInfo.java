public class PosInfo {
    private final Integer x;
    private final Integer y;
    private final Integer height;
    private final Integer width;

    public PosInfo(Integer x, Integer y, Integer height, Integer width) {
        this.x = x;
        this.y = y;
        this.height = height;
        this.width = width;
    }
}
